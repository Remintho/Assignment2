    private void tableMenuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMenuMouseClicked
        DefaultTableModel tblModel = (DefaultTableModel)tableMenu.getModel();
        int selectedRowIndex = tableMenu.getSelectedRow();
        bedTypeField.setEditable(false);
        bedTypeField.setText(tblModel.getValueAt(selectedRowIndex, 0).toString());
        roomNumber.removeAllItems();
        for(Object value: reserve.getRoomNumber(bedTypeField.getText())){
            roomNumber.addItem((Integer) (value));
        }
    }//GEN-LAST:event_tableMenuMouseClicked

    
