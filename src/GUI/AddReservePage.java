    private void checkInActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_checkInActionPerformed
        returnLabel. setHorizontalAlignment(SwingConstants. CENTER);
        String PAYMENT_STATUES = " ";
        if(paymentBox.getSelectedIndex()==0){
            PAYMENT_STATUES = "payed";
        }
        else if(paymentBox.getSelectedIndex()==1){
            PAYMENT_STATUES = "notpayed";
        }
        if(roomNumber.getSelectedIndex() == -1){
            returnLabel.setText("Please choose bed type then room number");
            returnLabel.setForeground(Color.red);
        }
        else if(checkInDate.getDate()==null){
            returnLabel.setText("Please choose check-In date");
            returnLabel.setForeground(Color.red);
        }
        else if(!reserve.checkInDates(checkInDate)){
            returnLabel.setText("Please Enter dates starting from todays date and forward");
            returnLabel.setForeground(Color.red);
        }
        else if(checkOutDate.getDate()==null){
            returnLabel.setText("Please choose check-Out date");
            returnLabel.setForeground(Color.red);
        }
        else if(!reserve.checkoutDates(checkOutDate)){
            returnLabel.setText("Please Enter date of atleast a day after your checkIn date");
            returnLabel.setForeground(Color.red);
        }
        else if (!(roomNumber.getSelectedIndex() == -1 || checkInDate.getDate()==null || checkOutDate.getDate()==null)){
            format = new SimpleDateFormat("yyyy-MM-dd");
            String check_In = format.format(checkInDate.getDate());
            String checkOut = format.format(checkOutDate.getDate());
            reserve.addReserve(userID, roomNumber.getSelectedItem().toString(), check_In, checkOut, PAYMENT_STATUES);
            returnLabel.setText("you've successfully booked a room");
            returnLabel.setForeground(Color.blue);
            checkInDate.setDate(null);
            checkOutDate.setDate(null);
            roomNumber.removeAllItems();
            bedTypeField.setText("selected Bed Type");
            this.dispose();
        }
    }//GEN-LAST:event_checkInActionPerformed

